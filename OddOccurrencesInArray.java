// Problem 2 : OddOccurrencesInArray
// Find value that occurs in odd number of elements.
// Solution Report - https://app.codility.com/demo/results/trainingDSK7PE-93T/

class Solution {
    public int solution(int[] A) {
        int unique = 0;
        for(int i=0;i<A.length;i++)
        {
            unique^=A[i];
        }
        return unique;
    }
}
